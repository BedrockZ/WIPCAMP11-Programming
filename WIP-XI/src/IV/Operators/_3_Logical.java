public class _3_Logical {
    public static void main(String[] args) {
        int x = 20;
        
        System.out.println("|| : " + (x < 100 || x > 50));
        
        System.out.println("&& : " + (x > 2 && x < 2));
        
        System.out.println("Normal : " + (x >= 5));
        
        System.out.println("! : " + !(x >= 5));
    }
}
